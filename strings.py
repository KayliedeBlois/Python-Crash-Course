firstLine = "Hey Jude"
secondLine = "Don't make it bad"
thirdLine = "Take a sad song"
fourthLine = "And make it better"

print(firstLine)
print(secondLine)
print(thirdLine)
print(fourthLine)

# use \n to create new line

validString = "Hey Jude\nDon't make it bad\nTake a sad song\nAnd make it better"
print(validString)

# use triple quotes around multi line strings

veryValidString = """Hey Jude
Don't make it bad
Take a sad song
And make it better"""
print(veryValidString)